package com.yhj.zchat.bean.Agreement;

import lombok.Data;

/**
 * @FileName Packet
 * @Author Yihaojun
 * @date 2026-03-21
 * 完整数据包
 *  Header(16) + Meta(变长) + Payload(变长) + AuthTag(16)
 **/
@Data
public class ZPacket {
    // 协议头
    private ZHeader header;
    // 元数据
    private ZMeta meta;
    // 消息体（加密/压缩后的二进制）
    /**
     *  拆包器：从字节流中"切"出一个完整包
     *   解码器：把完整包"解析"成对象
     *
     *   为什么 Payload 变长不影响？
     *
     *   看新协议：
     *
     *   ┌────────────┬─────────────────────────────────────┐
     *   │  Header    │         ContentLength = 53          │
     *   │  16字节     │   (Meta + Payload + AuthTag 总和)    │
     *   └────────────┴─────────────────────────────────────┘
     *         ↓
     *   拆包器：完整包 = 16 + 53 = 69字节，一刀切！
     *
     *   拆包器根本不需要知道 Payload 多长！
     *
     *   它只需要知道："Header 后面还有多少字节" → ContentLength 告诉它了。
     *
     *   解码器才需要知道 Payload 长度
     *
     *   解码器拿到完整包后，才开始"拆解内部结构"：
     *
     *   第1步：读 Header → 知道 ContentLength = 53
     *   第2步：读 Meta 前 2 字节 → 知道 MetaLength = 26
     *   第3步：算出 Payload 长度 = 53 - 26 - 16 = 11
     *
     *   打个比方
     *
     *   拆包器 = 收快递的
     *
     *   快递单写着：包裹总重 5kg
     *   收快递的人：好，我收 5kg，任务完成
     *   （不需要知道里面有几件衣服、几本书）
     *
     *   解码器 = 拆快递的
     *
     *   拆开后发现：
     *   - 包装盒 = 0.5kg（Meta）
     *   - 衣服 = 3.5kg（Payload）
     *   - 防震泡沫 = 1kg（AuthTag）
     *   总共 = 5kg ✓
     *
     *   总结
     *
     *   ┌──────────────┬──────────────┬───────────────────────────────────────────┐
     *   │     阶段     │  需要知道的  │                谁提供信息                 │
     *   ├──────────────┼──────────────┼───────────────────────────────────────────┤
     *   │ 拆包（切包） │ 完整包总长度 │ Header.ContentLength                      │
     *   ├──────────────┼──────────────┼───────────────────────────────────────────┤
     *   │ 解码（解析） │ Meta 长度    │ Meta.metaLength                           │
     *   ├──────────────┼──────────────┼───────────────────────────────────────────┤
     *   │ 解码（解析） │ Payload 长度 │ 算出来的：ContentLength - MetaLength - 16 │
     *   └──────────────┴──────────────┴───────────────────────────────────────────┘
     *
     *   Payload 变长没关系，因为 ContentLength 已经把它包进去了，解码器拿到完整包后自己算就行。
     */
    private byte[] payload;
    // 认证标签 16字节
    private byte[] authTag;
}
